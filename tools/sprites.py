def new_canvas(w, h, bg=(0,0,0)):
    return Image.new('RGB', (w, h), bg)

def P(img, x, y, w, h, c):
    d = ImageDraw.Draw(img)
    d.rectangle([x, y, x+w-1, y+h-1], fill=c)

