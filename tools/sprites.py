def draw_text(img, s, x, y, c, scale=1):
    cx = x
    for ch in s.upper():
        if ch == ' ':
            cx += 3*scale; continue
        g = FONT.get(ch)
        if not g:
            cx += 4*scale; continue
        rows = g.split('|')
        for r, row in enumerate(rows):
            for q, cell in enumerate(row):
                if cell == 'X':
                    P(img, cx+q*scale, y+r*scale, scale, scale, c)
        cx += (len(rows[0])+1)*scale
    return cx - x
def text_w(s, scale=1):
    w = 0
    for ch in s.upper():
        if ch == ' ': w += 3*scale; continue
        g = FONT.get(ch)
        w += (len(g.split('|')[0])+1)*scale if g else 4*scale
    return w

# ---------- sprite drawing from string art ----------
def draw_sprite(img, art, legend, x, y, scale=1):
    rows = [r for r in art.strip('\n').split('\n')]
    for r, row in enumerate(rows):
        for q, ch in enumerate(row):
            if ch == '.' or ch == ' ':
                continue
            c = legend.get(ch)
            if c:
                P(img, x+q*scale, y+r*scale, scale, scale, c)

